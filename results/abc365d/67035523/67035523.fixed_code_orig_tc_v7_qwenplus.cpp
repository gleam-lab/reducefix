char win(char x) {
    if (x == 'R') return 'P';
    if (x == 'S') return 'R';
    if (x == 'P') return 'S';
    return 'X'; // shouldn't happen
}

int main() {
    int n;
    string s;
    cin >> n >> s;
    
    if (n == 0) {
        cout << 0 << endl;
        return 0;
    }
    
    int ans = 1; // Start with first move requiring a change
    char current = win(s[0]); // What we're currently playing
    
    for (int i = 1; i < n; i++) {
        char desired = win(s[i]);
        if (desired != current) {
            ans++;
            current = desired;
        }
    }
    
    cout << ans << endl;
    return 0;
}