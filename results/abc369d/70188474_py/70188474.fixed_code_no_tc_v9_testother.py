n = int(input())
a = list(map(int, input().split()))

# dp[i][0]: 最後に通常で倒した場合の最大経験値（i体目まで）
# dp[i][1]: 最後に倍点で倒した場合の最大経験値（i体目まで）
# 倍点は偶数番目の倒したモンスターにのみ発生するので、
# 「倍点が適用されるかどうか」ではなく「今までに何体倒したかの偶奇」が必要

# しかし、上記のコードの状態設計は誤解を含んでいる。
# 正しい状態: 
# dp[i][j] := i体目まで処理して、今まで倒したモンスターの数が j (mod 2) であるときの最大経験値

dp = [[0, 0] for _ in range(n+1)]
# dp[0][0] = 0, dp[0][1] = -inf (初期状態: 0体倒している → 偶数)

for i in range(1, n+1):
    # i-1 番目のモンスターの強さ: a[i-1]
    # 選択肢1: モンスターiを倒さない
    option_skip = max(dp[i-1][0], dp[i-1][1])
    
    # 選択肢2: モンスターiを倒す
    # これまでに倒した数の偶奇を見て、次が何番目になるか決まる
    # これまでが偶数(0)なら、次は1体目(奇数)→追加なし
    # これまでが奇数(1)なら、次は偶数番目→追加で同じ分
    take_even_bonus = dp[i-1][1] + 2 * a[i-1]  # 前が奇数個 → 今が偶数番目
    take_no_bonus = dp[i-1][0] + a[i-1]        # 前が偶数個 → 今が奇数番目
    
    # 更新
    dp[i][0] = max(option_skip, take_even_bonus)  # 倒した後、合計が偶数個になるのはtake_even_bonusの場合
    dp[i][1] = max(option_skip, take_no_bonus)    # 倒した後、合計が奇数個になるのはtake_no_bonusの場合

print(max(dp[n][0], dp[n][1]))