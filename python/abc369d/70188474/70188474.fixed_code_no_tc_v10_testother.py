n = int(input())
a = list(map(int, input().split()))

# dp[i][0]: 最も最近の討伐が偶数番目でない（奇数番目の討伐で終わっている）場合の最大経験値
# dp[i][1]: 最も最近の討伐が偶数番目で終わっている場合の最大経験値
# ただし、「偶数番目」「奇数番目」は討伐した順序におけるもの

# 状態の意味を以下のように定義しなおす：
# dp[i][0] = i体目まで考慮して、これまでに討伐したモンスターの数が偶数（つまり次に討伐すれば奇数番目）
# dp[i][1] = i体目まで考慮して、これまでに討伐したモンスターの数が奇数（つまり次に討伐すれば偶数番目）

dp0 = 0  # 偶数回討伐した後の最大ポイント（次に倒すと奇数回目）
dp1 = -10**18  # 奇数回討伐した後の最大ポイント（次に倒すと偶数回目。初期状態では達成不可）

for i in range(n):
    # 現在のモンスターの強さ
    x = a[i]
    
    # 新しい状態を計算（更新時に古い状態を使い回さないよう注意）
    new_dp0 = dp0  # このモンスターをスルー（討伐数不変）
    new_dp1 = dp1
    
    # モンスターを倒す場合
    # 現在偶数回討伐済み → 次は奇数回目（報酬: x）
    new_dp1 = max(new_dp1, dp0 + x)
    
    # 現在奇数回討伐済み → 次は偶数回目（報酬: 2*x）
    new_dp0 = max(new_dp0, dp1 + 2 * x)
    
    dp0, dp1 = new_dp0, new_dp1

print(max(dp0, dp1))